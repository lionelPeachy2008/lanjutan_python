#operator bitwise

a = 9
b = 5

#bitwise OR |
c = a | b 
print("\n=====OR=====")
print("Nilai :",a, "Binary :",format(a,"08b"))
print("Nilai :",b, "Binary :",format(b,"08b"))
print("\n")
print("Nilai :",c, "Binary :",format(c,"08b"))
print("\n")

#bitwise AND &
c = a & b 
print("\n=====AND=====")
print("Nilai :",a, "Binary :",format(a,"08b"))
print("Nilai :",b, "Binary :",format(b,"08b"))
print("\n")
print("Nilai :",c, "Binary :",format(c,"08b"))
print("\n")

#bitwise XOR ^
c = a ^ b 
print("\n=====XOR=====")
print("Nilai :",a, "Binary :",format(a,"08b"))
print("Nilai :",b, "Binary :",format(b,"08b"))
print("\n")
print("Nilai :",c, "Binary :",format(c,"08b"))
print("\n")